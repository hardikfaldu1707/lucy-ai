import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const optionsDir = path.join(root, "public/create/options");
const outFile = path.join(root, "src/constants/create-option-uploaded.ts");

const categories = ["style", "hair-style", "hair-color", "body", "outfit"];
const uploaded = [];

for (const category of categories) {
  const dir = path.join(optionsDir, category);
  let files;
  try {
    files = await readdir(dir);
  } catch {
    continue;
  }
  for (const file of files) {
    if (!file.endsWith(".webp")) continue;
    uploaded.push(`${category}/${file.slice(0, -".webp".length)}`);
  }
}

uploaded.sort();

const contents = `/** Auto-generated by \`npm run sync:create-options\` — do not edit manually. */
export const CREATE_OPTION_UPLOADED = new Set<string>([
${uploaded.map((key) => `  "${key}",`).join("\n")}
]);
`;

await writeFile(outFile, contents, "utf8");
console.log(`Synced ${uploaded.length} create option image(s) → ${path.relative(root, outFile)}`);
